        // Elementos DOM
        const bankSelection = document.getElementById('bank-selection');
        const operationSelection = document.getElementById('operation-selection');
        const guideContainer = document.getElementById('guide-container');
        const backToBanksBtn = document.getElementById('back-to-banks');
        const backToOperationsBtn = document.getElementById('back-to-operations');
        const stepIndicator = document.getElementById('step-indicator');
        const stepImage = document.getElementById('step-image');
        const stepDescription = document.getElementById('step-description');
        const prevStepBtn = document.getElementById('prev-step');
        const nextStepBtn = document.getElementById('next-step');
        const guideTitle = document.getElementById('guide-title');
        const operationTitle = document.getElementById('operation-title');

        // Variáveis de estado
        let currentBank = null;
        let currentOperation = null;
        let currentStep = 0;
        let currentSteps = [];

        // Event Listeners para os bancos
        document.querySelectorAll('.bank-item').forEach(bank => {
            bank.addEventListener('click', () => {
                currentBank = bank.getAttribute('data-bank');
                showOperationSelection();
            });
        });

        // Event Listeners para as operações
        document.querySelectorAll('.option-btn').forEach(option => {
            option.addEventListener('click', () => {
                currentOperation = option.getAttribute('data-operation');
                loadGuide();
                showGuide();
            });
        });

        // Botões de navegação
        backToBanksBtn.addEventListener('click', showBankSelection);
        backToOperationsBtn.addEventListener('click', showOperationSelection);
        prevStepBtn.addEventListener('click', prevStep);
        nextStepBtn.addEventListener('click', nextStep);

        // Funções de navegação
        function showBankSelection() {
            bankSelection.classList.add('active');
            operationSelection.classList.remove('active');
            guideContainer.classList.remove('active');
            currentBank = null;
            currentOperation = null;
        }

        function showOperationSelection() {
            bankSelection.classList.remove('active');
            operationSelection.classList.add('active');
            guideContainer.classList.remove('active');
            operationTitle.textContent = `${guides[currentBank].name} - Selecione o tipo de operação`;
        }

        function showGuide() {
            bankSelection.classList.remove('active');
            operationSelection.classList.remove('active');
            guideContainer.classList.add('active');
        }

        // Funções do passo a passo
        function loadGuide() {
            if (guides[currentBank] && guides[currentBank][currentOperation]) {
                currentSteps = guides[currentBank][currentOperation].steps;
                currentStep = 0;
                
                // Atualiza o título
                guideTitle.textContent = `${guides[currentBank].name} - ${currentOperation.toUpperCase()}`;
                
                // Cria os indicadores de passo
                stepIndicator.innerHTML = '';
                for (let i = 0; i < currentSteps.length; i++) {
                    const dot = document.createElement('div');
                    dot.className = i === 0 ? 'step-dot active' : 'step-dot';
                    stepIndicator.appendChild(dot);
                }
                
                // Atualiza o conteúdo do primeiro passo
                updateStep();
            } else {
                // Caso não exista um guia, mostra mensagem de erro
                stepImage.innerHTML = '<i class="fas fa-exclamation-triangle"></i>';
                stepDescription.textContent = 'Guia não disponível para esta operação.';
                prevStepBtn.disabled = true;
                nextStepBtn.disabled = true;
            }
        }

        function updateStep() {
            // Atualiza a imagem e descrição
            const step = currentSteps[currentStep];
            stepImage.innerHTML = step.image;
            stepDescription.textContent = step.description;
            
            // Atualiza os indicadores de passo
            const dots = document.querySelectorAll('.step-dot');
            dots.forEach((dot, index) => {
                dot.classList.toggle('active', index === currentStep);
            });
            
            // Habilita/desabilita os botões de navegação
            prevStepBtn.disabled = currentStep === 0;
            nextStepBtn.disabled = currentStep === currentSteps.length - 1;
        }

        function prevStep() {
            if (currentStep > 0) {
                currentStep--;
                updateStep();
            }
        }

        function nextStep() {
            if (currentStep < currentSteps.length - 1) {
                currentStep++;
                updateStep();
            }
        }